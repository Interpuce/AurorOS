#include <drivers/ports.hpp>

void pic_remap() {
    // master PIC: 0x20, slave PIC: 0xA0
    outb(0x20, 0x11);
    outb(0xA0, 0x11);
    outb(0x21, 0x20); // remap IRQ0-7 -> int 0x20-0x27
    outb(0xA1, 0x28); // remap IRQ8-15 -> int 0x28-0x2F
    outb(0x21, 0x04);
    outb(0xA1, 0x02);
    outb(0x21, 0x01);
    outb(0xA1, 0x01);
    outb(0x21, 0x0);
    outb(0xA1, 0x0);
}